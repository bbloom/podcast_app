        /* 
        `->flip()` swaps the keys and values of the collection. So instead of:

        ```php
        [0 => 4, 1 => 5, 2 => 9, ...]  // index => episode_id
        ```

        You get:

        ```php
        [4 => 0, 5 => 1, 9 => 2, ...]  // episode_id => index
        ```

        The reason is performance. Without `flip()`, checking if an episode ID exists requires `in_array($id, $existingEpisodeIds)` which scans the entire array every time — O(n) per check.

        With `flip()`, the episode IDs become the **keys**, so `isset($existingEpisodeIds[$id])` is an O(1) hash lookup. With potentially hundreds of pivot rows to insert, that's meaningfully faster.

        The `->all()` at the end just converts the collection back to a plain PHP array, since `isset()` works on arrays.        
        */
        
